                    <h1 class="mb-3 heading-1">About Us</h1>                    
                    <p>Mintage World is a newly established online Museum based in Mumbai. Catering to Numismatists, Notaphilists and Philatelists, we are committed to a higher quality of services.</p>
                    <p>Mintage World was founded by Sri Sushilkumar Agrawal in 2015 who is also the Chairman & Managing Director of Ultra Media & Entertainment Pvt. Ltd. The purpose of this site is to impart knowledge and interest in the areas of Numismatics, Notaphily and Philately to the general public, collectors, students and scholars alike. Mintage World is the first website of its kind in the world where it provides all three domains under one roof.</p>
                    <p>Our team is composed of personnel with extensive experience in Numismatics, Notaphily and Philately. The site promotes research work among scholars as well as educating the general public. By educating the public it strives to create awareness among the people in all three fields. Our vision is to involve people for the preservation of India's vast and varied historical and cultural heritage through Coins, Bank Notes and Stamps.</p>
                    <p>It is our earnest desire that every visitor to this site would not leave empty handed but would be equipped with new knowledge of one's past and a pronounced interest in one or all three spheres.</p>

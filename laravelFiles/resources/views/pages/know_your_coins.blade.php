                        <span id="Collections-and-Auctions"></span>
                        <h5><b>Rarity</b></h5> 
                        <p>Rarity of a coin depends on the number of the type in circulation. If the number of coins belonging to a type is more, then the coin's rarity is common. If the coin is of a unique type or has limited pieces belonging to that type, then the coin is termed as rare. Rare coins are difficult to come across and are rarely photographed, but their value remains high in the market.</p>
                     
                        <h5><b>Collections and Auctions</b></h5> 
                        <p>Coin collectors usually focus on dynasties, rulers or types. A collection is complete if it includes coins of the same type belonging to different rulers or a coin of each type issued by a ruler or the dynasty. Coin collecting differs from Numismatics as it does not include studying the currency, but only collecting it. A numismatist may or may not be a coin collector and a collector may or may not be a numismatist.</p>
                        <p>Numerous numismatic auction houses have been catering to the numismatic community for a while now. They are the ones to go to if you wish to add a rare coin to your collection or start one. Common coins can be found with all coin dealers, however, rare coins are usually found only with auction houses. Auction houses have coin catalogues to help you identify and choose the coin you would like to buy. It is not, however, similar to a coin shop. If you wish to buy a coin from the auction house, you will be required to place a bid. The auctions may be conventional, silent or online. Almost all auction houses have established their presence on the internet and collectors and enthusiasts are able to place their bids from around the world at the click of a button.</p>
        
        
        
        
        
        

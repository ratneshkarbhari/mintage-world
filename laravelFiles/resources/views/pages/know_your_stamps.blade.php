                        <h5><b>Stamps in India</b></h5>   
                       <p>India has long and varied philatelic history. The postal authorities have issued a large number of stamps. A variety of techniques have been used to print the design including line engraving, typography, lithography, photogravure and web-offset.</p>
                       <p>Stamps are produced for postage as well as service or revenue. Postal authorities have also issued definitive and commemorative stamps along with perforated, non-perforated and miniature stamp sheets. Many cases of overprinting also exists.</p>
                       <p>The Scinde Dawk issued by postal authorities in India was round in shape while the Bombay Sappers stamp was triangular.  The Scinde Dawk was the first postage stamp to be issued in India.</p>
                       <p>Scinde Dawk is a very old postal system that operated in the Sindh region in present-day Pakistan. The term also refers to the first adhesive postal stamp in Asia. These stamps were the precursors of adhesive stamps used across the subcontinent and regions under the control of the British East India Company. The stamps were prepaid and were first issued on July 1, 1852. They bore the British East Indian Coat of Arms which was embossed on wafers of red sealing wax impressed upon paper. Since the sealing wax wafers disintegrated and cracked, the Scinde Dawks were replaced by a colourless design embossed on white paper which was not visible under light. The last of these stamps depicted a blue design embossed on white paper.</p>
                       <p>All variants of the Scinde Dawk stamps display a face of half Anna but are extremely rare. Less than 100 of these rare philatelic classics exist.</p>

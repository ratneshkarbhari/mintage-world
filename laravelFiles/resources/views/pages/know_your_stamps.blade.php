                        <h5><b>Introduction</b></h5> 
                       <p>A stamp is essentially a piece of paper that is issued by a postal authority to be purchased and affixed on mail as proof of postage payment. Stamps are typically printed on custom-made paper and generally depict a design of national and cultural importance along with its value or denomination on the front. The back has gum adhesive to facilitate application onto mail. They can be purchased from postal authorities or an authorized vendor.</p>

                   <h4 class="heading-2 pb-3"><b>New Security Features of INR 500</b></h4>
                   <img src="https://www.mintageworld.com/img/new-feature-500.png" class="img-fluid" alt=""> 
                   <p> The new INR 500 and INR 2000 notes that are released following the announcement of PMO India, Narendra Modi, have the following new features. </p>
                   <h6 class="mt-4"><i class="fa fa-arrow-circle-right"></i><strong> Obverse</strong></h6>
                   <ul>
                    <li><p><strong> 1 </strong> : See-through register in denominational numeral.</p></li>
                    <li><p> <strong>2</strong> : Latent image of the denomination numeral. </p></li>
                    <li><p> <strong> 3 </strong> : Denomination numeral in Devnagari. </p></li>
                    <li><p> <strong> 4  </strong> :  Mahatma Gandhi’s portrait in centre facing to right. </p></li>
                    <li><p> <strong> 5 </strong> : Windowed security thread that changes colours from green to blue when the note is tilted. </p></li>
                    <li><p> <strong> 6 </strong> : Guarantee clause, Governor's signature with promise clause and RBI emblem tilted towards the right </p></li>
                    <li><p> <strong> 7 </strong> : Portrait and electrotype watermarks. </p></li>
                    <li><p><strong> 8 </strong> : Number panel with numerals growing from small to big on the top left side and bottom right side. </p>
                    <li><p><strong> 9 </strong> : Denomination in numerals with rupee symbol in colour changing ink (green to blue) on the bottom right.  </p></li>
                    <li><p><strong> 10 </strong> : Ashoka pillar emblem on the right.  </p></li>
                    <li><p><strong>For visually impaired:  </strong>  </p></li>
                    <li><p>Intaglio or Raised printing of Mahatma Gandhi’s portrait, Ashokan pillar emblem and an identification mark. </p></li>
                    <li><p><strong> 11 </strong> :Circle with Rs 500 in raised print on the right.   </p></li>
                    <li><p><strong> 12 </strong> :Five bleed lines on left and right in raised print.    </p></li>
                   </ul>

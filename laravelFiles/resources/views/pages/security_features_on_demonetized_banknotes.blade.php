                   <h4 class="heading-2 pb-3"><b>Security Features on Demonetized Banknotes</b></h4>
                   <img src="https://www.mintageworld.com/img/security-feature.png" class="img-fluid" alt="">  
                   <h6 class="mt-4"><i class="fa fa-arrow-circle-right"></i><strong> Obverse</strong></h6>
                   <ul>
                    <li> 
                    <p>
                  <strong> Security Thread </strong>–The security thread can be seen to the left of Gandhi’s portrait on Demonetized banknotes. Notes which are recent issues carry a machine-readable windowed, demetalised, and clear text magnetic 3mm wide security thread. The thread has ‘RBI’ inscribed on it and the colour shifts from green to blue when seen from different angles. The ribbon lights up in fluorescent yellow on the reverse while the obverse lights up the text when viewed under UV light. On the reverse, the thread is observed as a continuous line when held against the light. The security thread on notes of denominations up to Rupees 50 is a windowed, overprinted silver 1mm line.
                    
                    </p>
                    
                    </li>  
                    
                     <li> 
                    <p>
                  <strong> See-Through Register  </strong>–You will find that the floral design printed on the obverse as well as the reverse of the banknote in the centre of the vertical band next to the watermark window has been replaced by the denomination in numerals. Part of the numeral is printed on the obverse while the rest is printed on the reverse in such a way that when the note is held against the light, you will be able to see the complete denominational numeral.
                    
                    </p>
                    
                    </li>  
                   <li> 
                    <p>
                  <strong> Electrolyte Watermark  </strong>–In addition to the Mahatma Gandhi watermark, the denomination appears in numerals next to Gandhi’s portrait in the watermarked window. This feature is present in all notes except Rs. 5.
                    
                    </p>
                    
                    </li>      
                    
                    <li> 
                    <p>
                  <strong> Omron Anti-Photocopying Feature  </strong>–This feature can be seen in the form of small yellow circles on either side of the legend ‘Reserve Bank of India’. It prevents counterfeiting as it gives a different shade when copied on a photocopy machine. This feature is present on notes of denominations of Rupees 50 and above.
                    
                    </p>
                    
                    </li>   
                     <li> 
                    <p>
                  <strong> Optically Variable Ink </strong>–This feature can be seen in the form of small yellow circles on either side of the legend ‘Reserve Bank of India’. It prevents counterfeiting as it gives a different shade when copied on a photocopy machine. This feature is present on notes of denominations of Rupees 50 and above.
                    
                    </p>
                    
                    </li>  
                    
                     <li> 
                    <p>
                  <strong> Latent Image </strong>–A vertical band is placed next to Gandhi’s portrait which contains a latent image depicting the denomination in numerals.
                    </p>
                    
                    </li> 
                     <li> 
                    <p>
                  <strong> Intaglio Printing </strong>–Gandhi’s portrait, the RBI seal, Guarantee and Promise text, Ashoka Pillar on the left, the Governor’s signature, and the identification mark are printed in intaglio to assist the visually impaired. Intaglio is a printing process that raises the content to make it noticeable by touch.
                    </p>
                    
                    </li> 
                      <li> 
                    <p>
                  <strong> Micro Lettering</strong>–In the area behind Gandhi's portrait, an additional security feature can be noticed. Microlettering of the term 'RBI' can be seen in this area for Rupee 5 and 10 notes. Notes of denomination Rupees 20 and above carry the microlettering 'RBI' followed by the denomination in numerals. This feature can be clearly seen with the help of a magnifying glass.
                    
                    </p></li> 
                    
                     
                                               
                </ul>
                   <img src="https://www.mintageworld.com/img/security-feature1.png" class="img-fluid" alt=""> 
                   <h6 class="mt-4"><i class="fa fa-arrow-circle-right"></i><strong> Reverse</strong></h6>
                   <ul>
                    <li> 
                      <p>
                    <strong> Year of Printing  </strong>–The year in which the note was issued is printed at the bottom of the note in the centre. Notes issued prior to 2005 do not carry the year in which it was issued. These notes have been withdrawn from circulation and are no longer considered valid and legal tender.
                      </p>
                      
                      </li> 
                    <li> 
                      <p>
                    <strong> Omron Anti-Photocopying Feature  </strong>–Like on the obverse of the note, this feature can be seen in the form of small yellow circles in the right-hand margin for denominations of Rupees 50 and above.
                      </p>
                      
                      </li> 
                    
                    
                    </ul>
                   <div class="page-title2">New Security</div>
                   <img src="https://www.mintageworld.com/img/new-security.jpg" class="img-fluid" alt="" > 
                   <h6 class="mt-4"><i class="fa fa-arrow-circle-right"></i><strong> Details of New Features:</h6>
                    <ul>
                        <li><i class="fa fa-caret-right"></i><strong> Ascending (Telescopic) size of numerals in numbering panels:</strong><p>Numerals in both the numbering panels of banknotes will be in ascending size from left to right, while the first three alpha-numeric characters will remain constant in size.</p></li>
                        <li><i class="fa fa-caret-right"></i><strong> Bleed lines:</strong><p>The banknotes of <i class="fa fa-inr"></i>500 denomination will have five angular bleed lines in three sets of 2-1-2 lines on the obverse in both, the upper left and the right hand edge of the banknote. Similarly, <i class="fa fa-inr"></i>1000 banknotes will have six angular bleed lines in four sets of 1-2-2-1 lines on the obverse in both, the upper left and right hand edge of the banknotes. These will facilitate identification of these notes by visually impaired persons.</p></li>
                        <li><i class="fa fa-caret-right"></i> <strong>Enlarged Identification Mark:</strong>
                        <p>The banknotes of <i class="fa fa-inr"></i>500 denomination will have five angular bleed lines in three sets of 2-1-2 lines on the obverse in both, the upper left and the right hand edge of the banknote. Similarly,<i class="fa fa-inr"></i>1000 banknotes will have six angular bleed lines in four sets of 1-2-2-1 lines on the obverse in both, the upper left and right hand edge of the banknotes. These will facilitate identification of these notes by visually impaired persons.</p>
                        
                        </li>                                
                    </ul>
                   <p class="text-danger">All security features along with their illustrations are displayed by RBI on their website.</p>

                        <div class="heading-2">Stamps on International Refugee Day</div> 
<p>Violence, wars, terrorist attacks and prosecutions have torn the world apart into pieces to such an extent that it is almost unimaginable to put them back together. Even as you are reading this, thousands of people across the globe are fighting it out to only live their lives peacefully. Innocent lives including children have been lost year-on-year and countries all over the world have condemned these inhuman acts.</p>
<p>The ones who have managed to escape death are not lucky either. They are in search of their dear ones or trying to mend their broken homes, between piles of debris. The deteriorating economic condition, scarcity of basic amenities like food, clothing, shelter and medical assistance only worsens the wounds that are trying to heal. The only option left with them is to leave their motherland and hope that things work in their favour once they have migrated.</p>
<p>Nations must understand the plight of individuals who were forced to take such an extreme step. The World needs to unite and stand by them during difficult times like these in the name of humanity and peace.</p>
<p>World Refugee Day is observed every year on 20th June since 2001 to raise awareness about the serious situations that refugees have to face. The day was proclaimed by the United Nations General Assembly on 4th December 2000 as the year 2001 marked the 50th anniversary of the 1951 Convention relating to the Status of Refugees. Before 2000, the African Refugee Day was celebrated officially by many countries on the same day. The Organization of African Unity (OAU) agreed to have International Refugee Day coincide with Africa Refugee Day.</p>
<p>A special theme is chosen each year where government officials, humanitarian aid workers, celebrities, civilians and the refugees come together to address this alarming issue. Each one is encouraged to share their support by attending related events, joining groups and speaking their hearts on social media.</p>
<p>Several commemorative stamps have been issued across the world to celebrate the true spirit of World Refugee Day. Stamps issued by Sudan depict a refugee with his child and his belongings with a symbolic portrayal of a tree in the background. Korea issued a lovely stamp showcasing pictures of smiling women and children with their hands above their heads, gesturing a home to live in. A stamp by Algeria also showcases a group of refugees walking ahead in search of a new home. A beautiful 1960 stamp by Italy depicts a refugee carrying an injured on his own shoulders, walking ahead with his child. India post ahs also issued two 5 paise stamps that feature a depiction of refugees making their way towards their unknown destination.</p>
<p>This World Refugee Day, let’s pray for peace forever and hope that there are no need for anybody to forcefully leave their homeland.</p>
